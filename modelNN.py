import torch
from torch import nn
from torch.utils.data import Dataset, DataLoader, random_split
import pandas as pd
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import numpy as np
import matplotlib.pyplot as plt

X = pd.read_csv('trainingfinal.csv')
y = pd.read_csv('arsenics.csv')
X_tensor = torch.tensor(X.values, dtype=torch.float32)
y_tensor = torch.tensor(y.values, dtype=torch.float32).view(-1, 1)

print("NaNs:", torch.isnan(X_tensor).any(), torch.isnan(y_tensor).any())
print("Infs:", torch.isinf(X_tensor).any(), torch.isinf(y_tensor).any())

class WaterDataset(Dataset):
    def __init__(self, X, y):
        self.X = X
        self.y = y

    def __len__(self):
        return len(self.X)

    def __getitem__(self, idx):
        return self.X[idx], self.y[idx]

full_dataset = WaterDataset(X_tensor, y_tensor)

# Data Splits
train_size = int(0.7 * len(full_dataset))
val_size = int(0.15 * len(full_dataset))
test_size = len(full_dataset) - train_size - val_size
train_dataset, val_dataset, test_dataset = random_split(full_dataset, [train_size, val_size, test_size])

train_loader = DataLoader(train_dataset, batch_size=32, shuffle=True)
val_loader = DataLoader(val_dataset, batch_size=32)
test_loader = DataLoader(test_dataset, batch_size=64)

# Model Architecture
class NeuralNetwork(nn.Module):
    def __init__(self):
        super().__init__()
        self.linear_relu_stack = nn.Sequential(
            nn.Linear(X.shape[1], 64),
            nn.ReLU(),
            nn.Linear(64, 32),
            nn.ReLU(),
            nn.Linear(32, 1)
        )

    def forward(self, x):
        return self.linear_relu_stack(x)

model = NeuralNetwork()
loss_fn = nn.MSELoss()
optimizer = torch.optim.Adam(model.parameters(), lr=0.001)

# Training Loop
num_epochs = 50
train_losses = []
val_losses = []

for epoch in range(num_epochs):
    model.train()
    epoch_train_loss = 0
    for X_batch, y_batch in train_loader:
        pred = model(X_batch)
        loss = loss_fn(pred, y_batch)

        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

        epoch_train_loss += loss.item()

    avg_train_loss = epoch_train_loss / len(train_loader)
    train_losses.append(avg_train_loss)

    # Validation
    model.eval()
    epoch_val_loss = 0
    with torch.no_grad():
        for X_val, y_val in val_loader:
            val_pred = model(X_val)
            val_loss = loss_fn(val_pred, y_val)
            epoch_val_loss += val_loss.item()

    avg_val_loss = epoch_val_loss / len(val_loader)
    val_losses.append(avg_val_loss)

    print(f"Epoch {epoch+1}: Train Loss = {avg_train_loss:.4f}, Val Loss = {avg_val_loss:.4f}")



def evaluate_model(model, dataloader, name="Set"):
    model.eval()
    y_preds = []
    y_trues = []

    with torch.no_grad():
        for X_batch, y_batch in dataloader:
            preds = model(X_batch)
            y_preds.append(preds.numpy())
            y_trues.append(y_batch.numpy())

    y_preds = np.vstack(y_preds)
    y_preds = np.expm1(y_preds)
    y_trues = np.vstack(y_trues)
    y_trues = np.expm1(y_trues)

    mse = mean_squared_error(y_trues, y_preds)
    mae_value = mean_absolute_error(y_trues, y_preds)
    r2 = r2_score(y_trues, y_preds)

    print(f"\n{name} MSE (Loss): {mse:.4f}")
    print(f"{name} MAE: {mae_value:.4f}")
    print(f"{name} RÂ² Score: {r2:.4f}")

# Final Evaluation
evaluate_model(model, train_loader, name="Train")
evaluate_model(model, val_loader, name="Validation")
evaluate_model(model, test_loader, name="Test")

import matplotlib.pyplot as plt

plt.figure(figsize=(8, 5))
plt.plot(train_losses, label='Train Loss')
plt.plot(val_losses, label='Validation Loss')
plt.xlabel('Epoch')
plt.ylabel('Loss (MSE)')
plt.title('Training & Validation Loss over Epochs')
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

